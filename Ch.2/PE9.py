# Algorithm
# 1. Ask user to enter temperature
# 2. Convert celsius to fahrenheit
# 3. Display results

# Ask user input
print("\nThis program will convert celsius to fahrenheit")
celsius = float(input("Enter temperature in celsius: "))

# Calculation
fahrenheit = (9 / 5) * celsius + 32

# Display results
print("Fahrenheit is ", fahrenheit, " degrees")
