import turtle

turtle.fillcolor('blue')
turtle.begin_fill()
turtle.penup()
turtle.goto(-165, -50)
turtle.pendown()
turtle.circle(50)

turtle.penup()
turtle.goto(-110, -100)
turtle.pendown()
turtle.circle(50)

turtle.penup()
turtle.goto(-55, -50)
turtle.pendown()
turtle.circle(50)

turtle.penup()
turtle.goto(0, -100)
turtle.pendown()
turtle.circle(50)

turtle.penup()
turtle.goto(55, -50)
turtle.pendown()
turtle.circle(50)
turtle.end_fill()

turtle.done()