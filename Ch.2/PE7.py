# 1. Ask user for the miles driven
# 2. Ask user the amount of gas used
# 3. Calculate MPG
# 4. Display result

# User input
miles_driven = int(input("\nEnter in miles driven: "))
gallons_used = int(input("Enter in gallons used: "))

# Calculation
MPG = miles_driven / gallons_used

# Display result
print("\nMPG: ", MPG)