import turtle

turtle.fillcolor('blue')
turtle.pencolor('red')
turtle.begin_fill()
turtle.setheading(60)
turtle.forward(200)
turtle.setheading(300)
turtle.forward(200)
turtle.setheading(180)
turtle.forward(200)
turtle.end_fill()
turtle.fillcolor('white')
turtle.begin_fill()
turtle.setheading(45)
turtle.forward(140)
turtle.setheading(315)
turtle.forward(140)
turtle.end_fill()

turtle.done()
